export function generateBlockId(): string {
  return `blk-${Date.now()}-${Math.random().toString(36).slice(2, 8)}`;
}

export function generatePageId(): string {
  return `pg-${Date.now()}-${Math.random().toString(36).slice(2, 8)}`;
}

export function normalizePageName(title: string): string {
  return title.toLowerCase().trim();
}

export function createEmptyBlock(
  pageId: string,
  parentId: string | null = null,
  order: string = 'a0',
  type: BlockType = 'paragraph',
): NoteBlock {
  const now = Date.now();
  return {
    id: generateBlockId(),
    type,
    content: '',
    parentId,
    pageId,
    order,
    collapsed: false,
    indent: parentId ? 1 : 0,
    properties: {},
    refs: [],
    createdAt: now,
    updatedAt: now,
  };
}

export function createNewPage(title: string, isJournal: boolean = false, journalDate?: string): NotePage {
  const now = Date.now();
  return {
    id: generatePageId(),
    title,
    name: normalizePageName(title),
    isJournal,
    journalDate,
    properties: {},
    tags: [],
    favorite: false,
    pinned: false,
    createdAt: now,
    updatedAt: now,
  };
}

export function getTodayJournalDate(): string {
  const d = new Date();
  return `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}-${String(d.getDate()).padStart(2, '0')}`;
}

export function extractPageLinks(content: string): string[] {
  // Strip HTML tags first, then find [[links]]
  const text = stripHtml(content);
  const regex = /\[\[([^\]]+)\]\]/g;
  const links: string[] = [];
  let match;
  while ((match = regex.exec(text)) !== null) {
    links.push(match[1]!);
  }
  return links;
}

/** Strip HTML tags to get plain text (for search, word count, etc.) */
export function stripHtml(html: string): string {
  if (!html) return '';
  return html.replace(/<[^>]*>/g, '');
}

export function orderBetween(before: string | null, after: string | null): string {
  if (!before && !after) return 'a0';
  if (!before) return 'a0';
  if (!after) {
    const last = before.charCodeAt(before.length - 1);
    if (last < 122) return before.slice(0, -1) + String.fromCharCode(last + 1);
    return before + '0';
  }
  return before + '5';
}

export function migrateBlock(block: Partial<NoteBlock>): NoteBlock {
  const base = createEmptyBlock(block.pageId ?? '', block.parentId ?? null, block.order ?? 'a0');
  const normalized: NoteBlock = {
    ...base,
    ...block,
    type: block.type ?? 'paragraph',
    content: block.content ?? '',
    collapsed: block.collapsed ?? false,
    indent: block.indent ?? (block.parentId ? 1 : 0),
    properties: block.properties ?? {},
    refs: block.refs ?? [],
    createdAt: block.createdAt ?? base.createdAt,
    updatedAt: block.updatedAt ?? base.updatedAt,
  };

  if (block.type) return normalized;

  const text = stripHtml(normalized.content);
  if (text.startsWith('# ')) return { ...normalized, type: 'heading', properties: { ...normalized.properties, level: '1' } };
  if (text.startsWith('## ')) return { ...normalized, type: 'heading', properties: { ...normalized.properties, level: '2' } };
  if (text.startsWith('### ')) return { ...normalized, type: 'heading', properties: { ...normalized.properties, level: '3' } };
  if (text.startsWith('- [ ] ') || text.startsWith('- [x] ')) return { ...normalized, type: 'todo' };
  if (text.startsWith('- ')) return { ...normalized, type: 'list-item' };
  if (text.startsWith('> ')) return { ...normalized, type: 'quote' };
  if (text.startsWith('---')) return { ...normalized, type: 'divider' };
  if (text.startsWith('```')) return { ...normalized, type: 'code' };
  return normalized;
}
