// ═══════════════════════════════════════════════════════════════════
// Transaction System (SiYuan-inspired)
// Operation-based undo/redo with batching
// ═══════════════════════════════════════════════════════════════════

export type OpAction =
  | 'insert'
  | 'update'
  | 'delete'
  | 'move'
  | 'setBlockType'
  | 'setBlockProps';

type OpPayload = Partial<
  Pick<NoteBlock, 'content' | 'type' | 'properties' | 'parentId' | 'order' | 'indent'>
> | NoteBlock;

export interface IOperation {
  action: OpAction;
  blockId: string;
  pageId: string;
  /** Snapshot data for undo — shape depends on action */
  data?: OpPayload;
  previousData?: OpPayload;
}

export interface Transaction {
  id: string;
  timestamp: number;
  doOps: IOperation[];
  undoOps: IOperation[];
}

